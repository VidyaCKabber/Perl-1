# Perl program to demonstrate Array's 
# creation and accessing the array's elements 
  
# Creation an array fruits 
@fruits = ("apple", "banana", "pineapple", "kiwi"); 
  
# printing the array 
print "@fruits\n"; 
  
# Prints the array's elements 
# one by one using index 
print "$fruits[0]\n"; 
print "$fruits[1]\n"; 
print "$fruits[2]\n"; 
print "$fruits[3]\n"; 
